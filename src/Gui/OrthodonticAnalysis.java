    private void showButtonLineOneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonLineOneActionPerformed
        Point origin = new Point(0,0);
        
        Point start = new Point(Integer.valueOf(startX.getText()),
                Integer.valueOf(startY.getText()));
        Point end = new Point(Integer.valueOf(endX.getText()), 
                Integer.valueOf(endY.getText()));
        
        if(m.getStartPointLineOne() == null){
            m.setStartPointLineOne(start);
            if(m.getEndPointLineOne() == null){
                m.setEndPointLineOne(end);
            }
        }
        
        if(VectorMath.isPositiveLine(start, end)){
            Point point = VectorMath.translateLine(origin, start, end);
            length1.setText(String.format("%.2f", VectorMath.createVector(point).getLength()));
        }
        else{
            Point newEnd = VectorMath.convertNegativeLineToPositive(start, end);
            Point point = VectorMath.translateLine(origin, end, newEnd);
            length1.setText(String.format("%.2f", VectorMath.createVector(point).getLength()));
        }
        /**
         * When we measure lines we must have a common point, a reference point of measurement
         * Every line length is measured by using the Pythagorean theorem. a2+b2 = c2;
         * But there is a catch here; As we know a Point is defined by x,y values. Now lets
         * suppose we have two parallel lines with the same length(Physical length). If we use only
         * the Pythagorean theorem we get different values for equal lines, if one of the lines has
         * greater x or y values. This is why we make the lines start at 0.0.
         */
        Double horizontalAngle = VectorMath.GetAngleOfLineBetweenTwoPoints(start, end);
        horizontal1.setText(String.format("%.2f", horizontalAngle));
        ch.drawLine(1);
        activityLog.append(guiHandler.logLine(start, end));
        
    }//GEN-LAST:event_showButtonLineOneActionPerformed

